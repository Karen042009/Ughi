# Վայրերի տեսակները
PLACE_TYPES = sorted(list(set(place["type"] for place in ARMENIAN_PLACES)))

def get_places_by_type(place_type):
    """Վերադարձնում է տվյալ տեսակի բոլոր վայրերը"""
    return [place for place in ARMENIAN_PLACES if place["type"] == place_type]

def get_places_by_city(city_name):
    """Վերադարձնում է տվյալ քաղաքի բոլոր վայրերը"""
    city_places = []
    for place in ARMENIAN_PLACES:
        if city_name.lower() in place["name"].lower():
            city_places.append(place)
    return city_places

def get_random_places(count=10):
    """Վերադարձնում է պատահական վայրեր"""
    import random
    return random.sample(ARMENIAN_PLACES, min(count, len(ARMENIAN_PLACES)))

